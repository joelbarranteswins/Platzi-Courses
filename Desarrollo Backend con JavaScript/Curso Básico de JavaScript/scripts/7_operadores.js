// operadores aritméticos

// Suma
console.log(2 + 3) // 5
// Resta
console.log(5 - 3) // 2
// Multiplicación
console.log(4 * 2) // 8
// División
console.log(6 / 2) // 3
// Módulo (residuo de la división)
console.log(5 % 2) // 1

// operador de concatenación de strings
console.log("Hola" + " Platzi") // "Hola Platzi"


// operadores de asignación
var a = 1;
console.log(a) // 1

var saludo = "Hola Mundo"
console.log(saludo) // "Hola Mundo"

// operadores de comparación
// igualdad
console.log(3 == "3") // true
console.log(3 == 3) // true

// igualdad estricta (usa el tipo de dato)
console.log(3 === "3") // false
console.log(3 === 3) // true


// operadores de desigualdad
console.log(5 != "5") // false
console.log(5 !== "5") // true


// operadores de mayor o menos que
console.log(5 > 3) // true
console.log(5 < 3) // false
console.log(5 >= 6) // false
console.log(5 <= 6) // true


// operadores lógicos
//operador disyunción lógica
console.log(true && true) // true
console.log(true && false) // false

// operador union lógica
console.log(true || false) // true
console.log(false || false) // false

// operador de negación lógica
console.log(!true) // false
console.log(!false) // true

// operador de incremento y decremento
var a = 1
console.log(a++) // 1
console.log(a) // 2


// operador de decremento
var b = 2   
console.log(b--) // 2
console.log(b) // 1

// Operadores de asignación combinada
var a = 1
console.log(a += 3) // 4
console.log(a -= 2) // 2

// operador negación lógico
var a = 5
console.log(!(a < 0)) // true