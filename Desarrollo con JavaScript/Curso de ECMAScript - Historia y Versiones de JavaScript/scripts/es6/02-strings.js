let hello = 'hello';
let world = 'world';
let epicPhrase = hello + ' ' + world + '!';
console.log(epicPhrase);

// Template literals
let epicPhrase2 = `${hello} ${world}!`;
console.log(epicPhrase2);

// Multi-line strings
let lorem = 'esto es un string \n' + 'con saltos de linea';
console.log(lorem);

// Template literals
let lorem2 = `esto es un string
con saltos de linea
`;
console.log(lorem2);